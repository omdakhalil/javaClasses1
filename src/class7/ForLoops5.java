package class7;

public class ForLoops5 {
    public static void main(String[] args) {

        for (int i = 0; i <=10 ; i++) {
            System.out.println("5 * " +i+" = "+(5*i));
        }
        System.out.println("***********************************");

        for (int i = 0; i <=12; i++) {
            System.out.println("6 *"+i+"="+(6*i));
        }
        for (int i = 0; i <=10 ; i++) {
            System.out.println("3 *"+i+"="+(3*i));
        }
        for (int i = 0; i <= 10; i++) {
            System.out.println("3 *"+8+"="+(8*i));

        }
    }
}
